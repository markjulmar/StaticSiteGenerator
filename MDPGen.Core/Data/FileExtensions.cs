namespace MDPGen.Core.Data
{
    /// <summary>
    /// Common file extensions
    /// </summary>
    public static class FileExtensions
    {
        /// <summary>
        /// CSS files
        /// </summary>
        public const string Css = ".css";
        /// <summary>
        /// Minified CSS files
        /// </summary>
        public const string CssMin = ".min.css";
        /// <summary>
        /// Map for CSS file
        /// </summary>
        public const string CssMap = ".css.map";
        /// <summary>
        /// HTML files
        /// </summary>
        public const string Html = ".html";
        /// <summary>
        /// JavaScript files
        /// </summary>
        public const string Js = ".js";
        /// <summary>
        /// Minified JavaScript files
        /// </summary>
        public const string JsMin = ".min.js";
        /// <summary>
        /// Markdown files
        /// </summary>
        public const string Markdown = ".md";
        /// <summary>
        /// File explicitly marked to be ignored.
        /// </summary>
        public const string Ignore = ".ignore";
        /// <summary>
        /// SCSS file needs to be converted to CSS
        /// </summary>
        public const string Scss = ".scss";
    }
}


